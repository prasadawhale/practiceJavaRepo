import java.util.*;

//average of A,B,C
public class q1 {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int A = sc.nextInt();
        int B = sc.nextInt();
        int C = sc.nextInt();
        int sum = (A + B + C);
        int avg = sum / 3;
        System.out.println("average is: " + avg);

    }

}
